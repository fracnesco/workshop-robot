# Condizionali

# la struttura if-else è molto comune nella programmazione
# innanzitutto abbiamo if, una funzione che fa qualcosa solo se
# una condizione è soddisfatta

if (5 > 4)
  puts("AAA")
  sample :drum_bass_hard
end

sleep 0.5

if (4 > 5)
  puts("BBB")
  sample :drum_cymbal_closed
end

sleep 0.5

if (4 == 4)
  puts ("CCC")
  sample :drum_cowbell
end
